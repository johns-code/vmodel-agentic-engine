@dataclass(frozen=True)
class AutopilotResult:
    repository: str
    branch: str
    pull_request_url: str
    local_tests_passed: bool
    local_test_output: str
    reports: list[str]
    changed_files: list[str]

    def to_dict(self) -> dict[str, object]:
        return asdict(self)


def progress_delivered_run(run_dir: Path) -> AutopilotResult:
    delivery_path = run_dir / "delivery-result.json"
    if not delivery_path.exists():
        raise RuntimeError(f"delivery result not found: {delivery_path}")
    delivery = json.loads(delivery_path.read_text(encoding="utf-8"))
    checkout_dir = run_dir / "delivery-checkout"
    if not (checkout_dir / ".git").exists():
        raise RuntimeError(f"delivery checkout not found: {checkout_dir}")

    repository = str(delivery["repository"])
    branch = str(delivery["branch"])
    pr_url = str(delivery["pull_request"]["url"])
    package = json.loads((run_dir / "artifacts" / "artifact-package.json").read_text(encoding="utf-8"))

    _ensure_git_identity(checkout_dir)
    _checkout_branch(checkout_dir, branch)
    _write_product_ci(checkout_dir)
    changed_files = write_plantspeak_vertical_slice(checkout_dir, package, delivery["issues"])
    local_test = _run_local_tests(checkout_dir)
    planning_docs = write_plantspeak_documentation(checkout_dir, package, delivery["issues"], local_test)
    quality_docs = write_artifact_quality_gates(checkout_dir)
    reports = write_autopilot_reports(checkout_dir, package, delivery["issues"], local_test)
    review_cycle_docs = write_artifact_review_cycle(checkout_dir)
    _remove_generated_noise(checkout_dir)
    changed_files.extend(planning_docs)
    changed_files.extend(quality_docs)
    changed_files.extend(reports)
    changed_files.extend(review_cycle_docs)
    _commit_all(checkout_dir, "Autopilot implement PlantSpeak dev-mode vertical slice")
    _push(checkout_dir, branch)
    _update_pr(repository, branch, pr_url, delivery["issues"], local_test)

    result = AutopilotResult(
        repository=repository,
        branch=branch,
        pull_request_url=pr_url,
        local_tests_passed=local_test.returncode == 0,
        local_test_output=(local_test.stdout + local_test.stderr).strip(),
        reports=reports,
        changed_files=changed_files,
    )
    (run_dir / "autopilot-result.json").write_text(json.dumps(result.to_dict(), indent=2) + "\n", encoding="utf-8")
    return result


def write_plantspeak_vertical_slice(repo_dir: Path, package: dict[str, object], issues: list[dict[str, object]]) -> list[str]:
    source_dir = repo_dir / "plantspeak"
    tests_dir = repo_dir / "tests"
    data_dir = source_dir / "data"
    source_dir.mkdir(parents=True, exist_ok=True)
    tests_dir.mkdir(parents=True, exist_ok=True)
    data_dir.mkdir(parents=True, exist_ok=True)

    requirements = list(package["software_requirements"])
    data_dir.joinpath("requirements.json").write_text(json.dumps(requirements, indent=2) + "\n", encoding="utf-8")
    data_dir.joinpath("issue_links.json").write_text(json.dumps(issues, indent=2) + "\n", encoding="utf-8")

    files = {
        repo_dir / ".gitignore": _gitignore(),
        repo_dir / "README.md": _readme(),
        source_dir / "__init__.py": '"""PlantSpeak dev-mode implementation generated by vmodel-agentic-engine."""\n\n__version__ = "0.2.0"\n',
        source_dir / "requirements.py": _requirements_py(),
        source_dir / "pins.py": _pins_py(),
        source_dir / "devices.py": _devices_py(),
        source_dir / "icd.py": _icd_py(),
        source_dir / "cli.py": _cli_py(),
        tests_dir / "test_requirements.py": _test_requirements_py(),
        tests_dir / "test_devices.py": _test_devices_py(),
        tests_dir / "test_icd.py": _test_icd_py(),
        tests_dir / "test_cli.py": _test_cli_py(),
    }
    written: list[str] = []
    for path, content in files.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        written.append(str(path.relative_to(repo_dir)))
    written.extend(["plantspeak/data/requirements.json", "plantspeak/data/issue_links.json"])
    return sorted(set(written))


