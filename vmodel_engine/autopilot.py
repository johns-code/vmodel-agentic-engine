def _run_local_tests(repo_dir: Path) -> subprocess.CompletedProcess[str]:
    install = subprocess.run(
        ["python", "-m", "pip", "install", "-e", ".[dev]"],
        cwd=repo_dir,
        capture_output=True,
        text=True,
        check=False,
    )
    if install.returncode != 0:
        return install
    return subprocess.run(["python", "-m", "pytest"], cwd=repo_dir, capture_output=True, text=True, check=False)


def _remove_generated_noise(repo_dir: Path) -> None:
    for pattern in ("__pycache__", ".pytest_cache", "*.egg-info"):
        for path in repo_dir.rglob(pattern):
            if ".git" in path.parts:
                continue
            if path.is_dir():
                shutil.rmtree(path, onexc=_make_writable)
            else:
                path.chmod(stat.S_IWRITE)
                path.unlink()


def _make_writable(function, path, excinfo) -> None:
    os.chmod(path, stat.S_IWRITE)
    function(path)


